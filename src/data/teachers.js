/**
 * Get all unique teacher names (for autocomplete suggestions).
 */
export function getTeacherNames() {
  return TEACHERS.map((t) => t.name);
}

/**
 * Find a teacher by name (case-insensitive).
 * Returns { name, designation, department } or undefined.
 */
export function findTeacher(name) {
  if (!name) return undefined;
  const lower = name.toLowerCase();
  return TEACHERS.find((t) => t.name.toLowerCase() === lower);
}
